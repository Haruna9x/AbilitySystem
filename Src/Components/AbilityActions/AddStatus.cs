using UnityEngine;
using AbilitySystem;

public class AddStatus : AbilityAction {

    public StatusEffect statusPrefab;

    public override void OnCastStarted() {
        caster.statusManager.AddStatus(statusPrefab, caster);    
    }

    public override void OnCastCompleted() {
        caster.statusManager.RemoveStatus(statusPrefab.name, caster);
    }

}